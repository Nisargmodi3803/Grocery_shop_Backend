package com.example.grocery_shop_backend.Dto;

import com.example.grocery_shop_backend.Entities.Cart;

public class OrderDTO
{
    private String mobile;
    private String name;
    private String email;
    private int cityId; // ID
    private String address;
    private String specialInstructions;
    private int paymentMode; // 1 => COD & 2 => Online
    private double totalAmount;
    private double couponDiscount;
    private Integer couponId; // ID
    private int deliveryTimeSlotId; // ID
    private String pincode;
    private Cart[] carts;

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getCityId() {
        return cityId;
    }

    public void setCityId(int cityId) {
        this.cityId = cityId;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getSpecialInstructions() {
        return specialInstructions;
    }

    public void setSpecialInstructions(String specialInstructions) {
        this.specialInstructions = specialInstructions;
    }

    public int getPaymentMode() {
        return paymentMode;
    }

    public void setPaymentMode(int paymentMode) {
        this.paymentMode = paymentMode;
    }

    public double getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(double totalAmount) {
        this.totalAmount = totalAmount;
    }

    public double getCouponDiscount() {
        return couponDiscount;
    }

    public void setCouponDiscount(double couponDiscount) {
        this.couponDiscount = couponDiscount;
    }

    public Integer getCouponId() {
        return couponId;
    }

    public void setCouponId(Integer couponId) {
        this.couponId = couponId;
    }

    public int getDeliveryTimeSlotId() {
        return deliveryTimeSlotId;
    }

    public void setDeliveryTimeSlotId(int deliveryTimeSlotId) {
        this.deliveryTimeSlotId = deliveryTimeSlotId;
    }

    public String getPincode() {
        return pincode;
    }

    public void setPincode(String pincode) {
        this.pincode = pincode;
    }

    public Cart[] getCarts() {
        return carts;
    }

    public void setCarts(Cart[] carts) {
        this.carts = carts;
    }
}
